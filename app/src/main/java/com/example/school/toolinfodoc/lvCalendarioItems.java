package com.example.school.toolinfodoc;

class lvCalendarioItems {
    int icono;
    String header;
    String titulo;
    String fecha;
    int antiguedad;

    lvCalendarioItems(String header, int icono, int antiguedad, String titulo, String fecha) {
        super();
        this.icono = icono;
        this.header = header;
        this.titulo = titulo;
        this.fecha = fecha;
        this.antiguedad = antiguedad;
    }
}
